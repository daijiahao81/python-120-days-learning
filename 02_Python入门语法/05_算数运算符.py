# 算数（数学）运算符
print("1+1=",1+1)
print("2-1=",2-1)
print("2*3=",2*3)
print("4/2=",4/2)  # 除法，结果是浮点数
print("11//2=",11//2)  # 整除，结果是整数
print("11%2=",11%2)  # 取余，结果是余数
print("9%2=",9%2)  # 取余，结果是余数
print("2**3=",2**3)  # 幂运算，2的3次方
