# 表白100天，每天都送10支玫瑰花，直到表白成功，用while嵌套语句
# 定义天数变量
i = 1
# 循环表白100天，第一百天成功
while i <= 100:
    print(f"今天是第{i}天，准备向小美表白！.....")
    # 定义送玫瑰花变量
    j = 1
    # 每次表白之前送给小美10朵玫瑰花
    while j <= 10:
        print(f"送给小美10朵玫瑰花！")
        j += 1
    print("小美我喜欢你，做我女朋友吧！")        
    i += 1
print(f"今天是第{i-1}天，表白成功")