# 比较运算符的使用
# ==, != , > , < , >= , <=
# 演示进行内容的相等比较
num1 = 10
num2 = 10
print(f"10 == 10的结果是：{num1 == num2}")

num1 = 10
num2 = 20
print(f"10 != 20的结果是：{num1 != num2}")

name1 = "张三"
name2 = "李四"
print(f"张三 == 李四的结果是：{name1 == name2}")

