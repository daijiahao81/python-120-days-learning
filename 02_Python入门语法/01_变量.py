# 变量练习题
# 定义一个变量，用来记录钱包余额
money = 50
print(f"当前钱包余额: {money}")
# 买了一个冰淇凌，花费了10原
money -= 10
print(f"购买冰淇凌后余额: {money}")
# 买了一个可乐，花费了5元
money -= 5
print(f"购买可乐后余额: {money}")
print(f"最终钱包余额: {money}")